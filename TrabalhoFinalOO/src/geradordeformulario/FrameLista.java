    private void addNumItem()
    {
        pos += 25;
        JLabel num = new JLabel();
        num.setVisible(true);
        num.setText(Integer.toString(numItem));
        num.setSize(100,40);
        num.setLocation(25, pos);
        numItem += 1;
        getContentPane().add(num);
        initComponents();
    }
    
    private void addTxtItem()
    {
        pos += 25;
        JTextField txt = new JTextField();
        txt.setVisible(true);
        txt.setSize(100,29);
        txt.setLocation(45, pos - 20);
        getContentPane().add(txt);
        l.InsereItem(txt.getText());
        q.SalvaLista(l);
        initComponents();
        
    }
    
